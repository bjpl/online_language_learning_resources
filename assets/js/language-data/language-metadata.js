/**
 * Get all language metadata
 * @returns {Array} Array of language metadata objects
 */
export function getLanguageMetadata() {
  return languageMetadata;
}

/**
 * Get metadata for a specific language by code
 * @param {string} code - Language code (e.g., 'dutch', 'french')
 * @returns {Object|null} Language metadata object or null if not found
 */
export function getLanguageByCode(code) {
  return languageMetadata.find(lang => lang.code === code) || null;
}

/**
 * Search languages by name (case-insensitive)
 * @param {string} query - Search query
 * @returns {Array} Array of matching language metadata objects
 */
export function searchLanguages(query) {
  const lowerQuery = query.toLowerCase();
  return languageMetadata.filter(lang =>
    lang.name.toLowerCase().includes(lowerQuery) ||
    lang.nativeName.toLowerCase().includes(lowerQuery)
  );
}

/**
 * Get total number of languages
 * @returns {number} Total count of languages
 */
export function getLanguageCount() {
  return languageMetadata.length;
}
